// ===== Helper functions =====
export function getCourseById(id: string): Course | undefined {
  return courses.find((c) => c.id === id);
}

export function getProfessorById(id: string): Professor | undefined {
  return professors.find((p) => p.id === id);
}

export function getReviewsByCourse(courseId: string): Review[] {
  return reviews.filter((r) => r.courseId === courseId);
}

export function getResourcesByCourse(courseId: string): Resource[] {
  return resources.filter((r) => r.courseId === courseId);
}

export function getCoursesByProgram(programCode: string): Course[] {
  return courses.filter((c) => c.programCode === programCode);
}

export function getCoursesByProfessor(professorId: string): Course[] {
  return courses.filter((c) => c.professorId === professorId);
}

export function getProfessorInitials(name: string): string {
  return name
    .replace(/^(Prof\.?|Prof\.ssa\.?)\s*/i, '')
    .split(' ')
    .filter((n) => n.length > 0)
    .map((n) => n[0].toUpperCase())
    .slice(0, 2)
    .join('');
}

export function getUserReviews(author: string): Review[] {
  return reviews.filter((r) => r.author === author);
}
